        }
    }
}



// Redirect logged-in users from login or register pages to the dashboard
function redirect_logged_in_users() {
    // Check if user is logged in
    if (is_user_logged_in()) {
        // Get the current URL path
        $current_url = $_SERVER['REQUEST_URI'];

        // Check if the current URL is the login or register page
        if (strpos($current_url, '/login') !== false || strpos($current_url, '/register') !== false) {
            // Redirect to the dashboard
            wp_redirect(home_url('/my-dashboard'));
            exit;
        }
    }
}
add_action('template_redirect', 'redirect_logged_in_users');



