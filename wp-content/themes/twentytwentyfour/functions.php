function handle_registration_form() {
    if (isset($_POST['register'])) {
        global $wpdb;
        
        // Retrieve form data
        $first_name = sanitize_text_field($_POST['first_name']);
        $last_name = sanitize_text_field($_POST['last_name']);
        $email = sanitize_email($_POST['email']);
        $designation = sanitize_text_field($_POST['designation']);
        $country = sanitize_text_field($_POST['country']);
        $username = sanitize_user($_POST['username']);
        $password = $_POST['password'];
        
        // Additional fields for institution
        $represent_institution = isset($_POST['represent_institution']);
        $institution_name = $represent_institution ? sanitize_text_field($_POST['institution_name']) : '';
        
        // Insert user data into WordPress database
        $user_id = wp_create_user($username, $password, $email);
        
        if (!is_wp_error($user_id)) {
            // Save additional user meta data
            update_user_meta($user_id, 'first_name', $first_name);
            update_user_meta($user_id, 'last_name', $last_name);
            update_user_meta($user_id, 'designation', $designation);
            update_user_meta($user_id, 'country', $country);
            update_user_meta($user_id, 'represent_institution', $represent_institution);
            
            if ($represent_institution) {
                update_user_meta($user_id, 'institution_name', $institution_name);
               
            }

// Handle image upload
if (!empty($_FILES['profile_image']['name'])) {
	require_once(ABSPATH . 'wp-admin/includes/file.php');
	require_once(ABSPATH . 'wp-admin/includes/image.php');
	require_once(ABSPATH . 'wp-admin/includes/media.php');
	
	// Upload the image
	$image_id = media_handle_upload('profile_image', 0);

	if (is_wp_error($image_id)) {
		echo "Error uploading image: " . $image_id->get_error_message();
	} else {
		// Get the absolute file path of the uploaded image
		$image_path = get_attached_file($image_id);
		
		// Save the image path as user meta
		update_user_meta($user_id, 'profile_image_path', $image_path);
		echo "Image uploaded and path saved successfully!";
	}
}

            
